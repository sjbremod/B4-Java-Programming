package practice;

public class EscapeSequence {
    public static void main(String[] args){

        /*

        \n - new line character
        \t - tab character
        \' - single quote character
        \" - double quote character
        \\ - backslash character

         */

        System.out.println("welcome to loop academy, \nwe are here to help you");
        System.out.println("\twelcome to loop academy, we are here to help you");
        System.out.println("welcome to \"loop academy\", we are here to help you");
        System.out.println("\\");
        System.out.println("\'");

    }
}
