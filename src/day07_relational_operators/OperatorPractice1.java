package day07_relational_operators;

public class OperatorPractice1 {
    public static void main(String[] args) {
        int b = 12;
        b++;
        System.out.println(b);
        ++b;
        System.out.println(b);

        b--;
        System.out.println(b);
        --b; //
        System.out.println(b);
        System.out.println(b++);
        System.out.println(b);

        System.out.println(++b);
        System.out.println(b);
        System.out.println(--b);
        --b;
        System.out.println(--b);
        b--;
        System.out.println(b);


    }
}
