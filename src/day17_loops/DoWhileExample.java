package day17_loops;

public class DoWhileExample {
    public static void main(String[] args) {


        //do while loop
        int n = 1;
        do{
            System.out.println("do while " +n);
            n++;
        }while(n<=10);

        //while loop
        int m = 1;
        while(m<=10){
            System.out.println("while " +m);
            m++;

        }

    }
}
