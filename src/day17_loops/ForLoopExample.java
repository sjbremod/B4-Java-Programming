package day17_loops;

public class ForLoopExample {
    public static void main(String[] args) {

        //initialization --> int i = 0;
        //condition --> true/false
        // iteration --> cycles
       // for( initialization; condition; iteration ){
        for( int i = 0; i <=10 ; i++  ){
            System.out.println(i);
        }
    }
}
