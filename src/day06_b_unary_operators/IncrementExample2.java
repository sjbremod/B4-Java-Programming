package day06_b_unary_operators;

public class IncrementExample2 {
    public static void main(String[] args) {
        int age = 30;
        System.out.println(age);
        System.out.println(age++);
        System.out.println(age);

        System.out.println(age++);
        System.out.println(age++);
        System.out.println(age);
        System.out.println(++age);
        System.out.println(--age);
        System.out.println(age);
        System.out.println(age--);
        System.out.println(age);



    }
}
